def player_moves(key, player):
    if key[pygame.K_DOWN]:
        player.rect.top += 50
    elif key[pygame.K_UP]:
        player.rect.top -= 50
    elif key[pygame.K_RIGHT]:
        if player.rect.left + 50 > WIDTH:
            player_group.empty()
            player, level_x, level_y = generate_level(load_level('map1.txt'))
        else:
            player.rect.left += 50
    elif key[pygame.K_LEFT]:
        player.rect.left -= 50
    return player


